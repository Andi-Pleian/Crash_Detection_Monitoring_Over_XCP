/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
void initLED(void) {
    IfxPort_setPinModeOutput(LED_P20_12, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);
    IfxPort_setPinHigh(LED_P20_12);
}

void blinkLED(void) {
    IfxPort_togglePin(LED_P20_12);
}
